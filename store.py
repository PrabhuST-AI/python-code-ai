# Variables and Data Types
item_name = "Coffee Maker"  # String
price = 100                 # Int
is_on_sale = True           # Bool

# Basic Math & Conditionals (If-Else)
if is_on_sale:
    discounted_price = price - 20
    print(f"The {item_name} is on sale for ${discounted_price}!")
else:
    print(f"The {item_name} is normal price: ${price}.")